def build_tsss_search_brief(subject_asset: SubjectAsset) -> dict[str, Any]:
    """Build a research brief for the given subject asset.

    Returns a structured dict with subject asset summary and a Vietnamese brief.
    Does NOT call LLM; purely deterministic.
    """
    return {
        "subject_asset": {
            "address": subject_asset.address,
            "market_area": subject_asset.market_area,
            "land_type": subject_asset.land_type,
            "planning_segment": subject_asset.planning_segment,
            "size_sqm": subject_asset.size_sqm,
        },
        "brief": (
            f"Tìm kiếm bất động sản so sánh tại khu vực {subject_asset.market_area}, "
            f"loại đất {subject_asset.land_type}, "
            f"diện tích khoảng {subject_asset.size_sqm} m²."
        ),
    }


def suggest_search_queries(subject_asset: SubjectAsset) -> dict[str, Any]:
    """Generate search query suggestions using OmniRoute LLM.

    Returns structured dict with:
      - search_queries: list of suggested search query strings
      - market_area / land_type / size_range_min / size_range_max / price_basis
      - source_caution: warning about source verification
      - warnings: list if LLM fallback was used

    On LLM failure, returns deterministic fallback with warnings.
    """
    query_hint = _subject_query_hint(subject_asset)

    try:
        client = OmniRouteClient()
        system_msg = (
            "Bạn là trợ lý nghiên cứu thẩm định giá (TSSS Valuation Assistant). "
            "Luôn trả lời bằng tiếng Việt. "
            "Trả về JSON hợp lệ với các key: "
            "search_queries (mảng 5 chuỗi), market_area, land_type, "
            "size_range_min (số hoặc null), size_range_max (số hoặc null), "
            "price_basis (string), source_caution (string)."
        )
        user_msg = (
            f"Tài sản thẩm định:\n"
            f"- Địa chỉ: {subject_asset.address}\n"
            f"- Khu vực: {subject_asset.market_area}\n"
            f"- Loại đất: {subject_asset.land_type}\n"
            f"- Quy hoạch: {subject_asset.planning_segment or 'N/A'}\n"
            f"- Diện tích: {subject_asset.size_sqm} m²\n\n"
            f"Đề xuất 5 truy vấn tìm kiếm bất động sản so sánh phù hợp. "
            f"Xác định market_area, land_type, khoảng diện tích, "
            f"cơ sở giá và cảnh báo nguồn."
        )
        messages = [
            {"role": "system", "content": system_msg},
            {"role": "user", "content": user_msg},
        ]
        result = client.chat_completion(messages, temperature=0.3, response_format_json=True)
        content = result.get("choices", [{}])[0].get("message", {}).get("content", "{}")
        parsed = json.loads(content)

        # Ensure all keys exist
        return {
            "search_queries": parsed.get("search_queries", query_hint["search_queries"]),
            "market_area": parsed.get("market_area", subject_asset.market_area),
            "land_type": parsed.get("land_type", subject_asset.land_type),
            "size_range_min": parsed.get("size_range_min"),
            "size_range_max": parsed.get("size_range_max"),
            "price_basis": parsed.get("price_basis", "VND/m²"),
            "source_caution": parsed.get(
                "source_caution",
                "Xác minh dữ liệu niêm yết độc lập trước khi sử dụng.",
            ),
            "warnings": [],
        }
    except (GatewayUnavailableError, json.JSONDecodeError, KeyError, TypeError) as exc:
        fallback = dict(_FALLBACK_QUERIES)
        fallback["search_queries"] = query_hint["search_queries"]
        fallback["market_area"] = subject_asset.market_area
        fallback["land_type"] = subject_asset.land_type
        fallback["size_range_min"] = max(0, subject_asset.size_sqm * 0.5)
        fallback["size_range_max"] = subject_asset.size_sqm * 2.0
        warning_msg = (
            f"OmniRoute LLM unavailable ({type(exc).__name__}: {exc}). "
            f"Using deterministic fallback."
        )
        fallback["warnings"] = [warning_msg]
        return fallback


def extract_candidate_from_raw_text(
    raw_text: str,
    source_url: str | None,
    subject_asset: SubjectAsset,
) -> dict[str, Any]:
    """Extract normalized candidate fields from raw listing text using OmniRoute LLM.

    Args:
        raw_text: Raw listing text (required).
        source_url: Optional source URL.
        subject_asset: The subject asset for context.

    Returns:
        dict with extracted fields (market_area, land_type, planning_segment,
        size_sqm, unit_price, asking_price, source_quality, draft_note) and
        warnings list. Missing fields are null.

    On LLM failure, returns deterministic fallback with warnings.
    """
    if not raw_text or not raw_text.strip():
        fallback = dict(_FALLBACK_EXTRACT)
        fallback["warnings"] = ["raw_text is empty or missing. No extraction possible."]
        if source_url:
            fallback["source_url"] = source_url
        return fallback

    try:
        client = OmniRouteClient()
        system_msg = (
            "Bạn là trợ lý trích xuất dữ liệu thẩm định giá (TSSS Extraction Assistant). "
            "Trích xuất thông tin bất động sản từ văn bản niêm yết. "
            "Trả về JSON hợp lệ. "
            "Nếu thông tin không có, trả về null cho trường đó. "
            "Không bịa đặt dữ liệu. "
            "Các trường: market_area (string), land_type (string), "
            "planning_segment (string hoặc null), size_sqm (number hoặc null), "
            "unit_price (number VND/m² hoặc null), asking_price (number VND hoặc null), "
            "source_quality ('HIGH'|'MEDIUM'|'LOW'|'WEAK'|null), "
            "draft_note (string mô tả ngắn bằng tiếng Việt)."
        )
        user_msg = (
            f"Tài sản thẩm định (context):\n"
            f"- Khu vực: {subject_asset.market_area}\n"
            f"- Loại đất: {subject_asset.land_type}\n"
            f"- Diện tích: {subject_asset.size_sqm} m²\n\n"
            f"Văn bản niêm yết:\n{raw_text}\n\n"
            f"Trích xuất thông tin bất động sản so sánh từ văn bản trên."
        )
        messages = [
            {"role": "system", "content": system_msg},
            {"role": "user", "content": user_msg},
        ]
        result = client.chat_completion(messages, temperature=0.1, response_format_json=True)
        content = result.get("choices", [{}])[0].get("message", {}).get("content", "{}")
        parsed = json.loads(content)

        extracted = {
            "market_area": parsed.get("market_area"),
            "land_type": parsed.get("land_type"),
            "planning_segment": parsed.get("planning_segment"),
            "size_sqm": _safe_float(parsed.get("size_sqm")),
            "unit_price": _safe_float(parsed.get("unit_price")),
            "asking_price": _safe_float(parsed.get("asking_price")),
            "source_quality": parsed.get("source_quality"),
            "draft_note": parsed.get("draft_note"),
        }

        if source_url:
            extracted["source_url"] = source_url

        # Collect warnings for missing critical fields
        warnings = []
        if not extracted["market_area"]:
            warnings.append("market_area not found in raw text.")
        if not extracted["land_type"]:
            warnings.append("land_type not found in raw text.")
        if not extracted["size_sqm"]:
            warnings.append("size_sqm not found in raw text.")
        if not extracted["unit_price"]:
            warnings.append("unit_price not found in raw text.")
        extracted["warnings"] = warnings

        return extracted

    except (GatewayUnavailableError, json.JSONDecodeError, KeyError, TypeError) as exc:
        fallback = dict(_FALLBACK_EXTRACT)
        if source_url:
            fallback["source_url"] = source_url
        warning_msg = (
            f"OmniRoute LLM unavailable ({type(exc).__name__}: {exc}). "
            f"Using deterministic fallback parsing."
        )
        fallback["warnings"] = [warning_msg]

        # Attempt deterministic parsing as fallback
        parsed = _fallback_parse_raw_text(raw_text, subject_asset)
        for key in ("market_area", "land_type", "planning_segment",
                     "size_sqm", "unit_price", "asking_price", "source_quality"):
            if parsed.get(key) is not None:
                fallback[key] = parsed[key]
                msg = f"Deterministic fallback resolved: {key}={parsed[key]}"
                fallback["warnings"].append(msg)

        return fallback


def generate_candidate_rationale(
    candidate: RawCandidate,
    subject_asset: SubjectAsset,
    rule_assessment: dict[str, Any] | None = None,
) -> dict[str, Any]:
    """Generate a rationale for a candidate using OmniRoute LLM.

    Args:
        candidate: The raw candidate to evaluate.
        subject_asset: The subject asset for comparison.
        rule_assessment: Optional dict with rule assessment results.

    Returns:
        dict with rationale, strengths, weaknesses, adjustment_notes, warnings.

    On LLM failure, returns deterministic fallback with warnings.
    """
    try:
        client = OmniRouteClient()
        rule_context = ""
        if rule_assessment:
            rule_context = (
                "\nKết quả đánh giá quy tắc:\n"
                f"{json.dumps(rule_assessment, ensure_ascii=False, indent=2)}"
            )

        system_msg = (
            "Bạn là trợ lý phân tích thẩm định giá (TSSS Rationale Assistant). "
            "Đánh giá bất động sản so sánh dựa trên tài sản thẩm định. "
            "Trả về JSON hợp lệ. "
            "Các trường: rationale (string), strengths (mảng string), "
            "weaknesses (mảng string), adjustment_notes (mảng string)."
        )
        user_msg = (
            f"Tài sản thẩm định:\n"
            f"- Địa chỉ: {subject_asset.address}\n"
            f"- Khu vực: {subject_asset.market_area}\n"
            f"- Loại đất: {subject_asset.land_type}\n"
            f"- Diện tích: {subject_asset.size_sqm} m²\n\n"
            f"Bất động sản so sánh:\n"
            f"- URL: {candidate.source_url or 'N/A'}\n"
            f"- Khu vực: {candidate.market_area or 'N/A'}\n"
            f"- Loại đất: {candidate.land_type or 'N/A'}\n"
            f"- Diện tích: {candidate.size_sqm or 'N/A'} m²\n"
            f"- Đơn giá: {candidate.unit_price or 'N/A'} VND/m²\n"
            f"- Giá chào: {candidate.asking_price or 'N/A'} VND\n"
            f"- Chất lượng nguồn: {candidate.source_quality or 'N/A'}"
            f"{rule_context}\n\n"
            f"Đưa ra đánh giá về tính phù hợp của bất động sản so sánh này."
        )
        messages = [
            {"role": "system", "content": system_msg},
            {"role": "user", "content": user_msg},
        ]
        result = client.chat_completion(messages, temperature=0.3, response_format_json=True)
        content = result.get("choices", [{}])[0].get("message", {}).get("content", "{}")
        parsed = json.loads(content)

        return {
            "rationale": parsed.get("rationale", ""),
            "strengths": parsed.get("strengths", []),
            "weaknesses": parsed.get("weaknesses", []),
            "adjustment_notes": parsed.get("adjustment_notes", []),
            "warnings": [],
        }

    except (GatewayUnavailableError, json.JSONDecodeError, KeyError, TypeError) as exc:
        fallback = dict(_FALLBACK_RATIONALE)
        warning_msg = (
            f"OmniRoute LLM unavailable ({type(exc).__name__}: {exc}). "
            f"Using deterministic fallback."
        )
        fallback["warnings"] = [warning_msg]
        return fallback


