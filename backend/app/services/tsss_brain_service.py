"""TSSS Brain Service — AI-assisted research workflow for TSSS valuation.

OmniRoute is the LLM gateway only. This service does NOT claim it performs
verified web search by itself. The system clearly separates:
1. Search query suggestions generated by LLM.
2. Candidate raw text/URL provided by user or future crawler.
3. Extracted/normalized comparable data.
4. Hard-filter/scoring/final analyst selection.
"""

import json
import re
from typing import Any

from app.models.candidate import RawCandidate
from app.models.project import SubjectAsset
from app.services.omni_client import GatewayUnavailableError, OmniRouteClient

# ---------------------------------------------------------------------------
# Deterministic fallback responses when LLM is unavailable
# ---------------------------------------------------------------------------

_FALLBACK_QUERIES = {
    "search_queries": [
        "Nhà đất bán tại khu vực [market_area], loại đất [land_type]",
        "Đất [land_type] thổ cư [market_area] diện tích [size] m²",
        "Bất động sản [land_type] [market_area] giá tốt",
        "So sánh giá đất [land_type] khu vực [market_area]",
        "Giao dịch [land_type] thành công [market_area]",
    ],
    "market_area": None,
    "land_type": None,
    "size_range_min": None,
    "size_range_max": None,
    "price_basis": "VND/m²",
    "source_caution": (
        "Verify listing data independently. "
        "Do not rely solely on asking prices without cross-checking."
    ),
}

_FALLBACK_EXTRACT = {
    "market_area": None,
    "land_type": None,
    "planning_segment": None,
    "size_sqm": None,
    "unit_price": None,
    "asking_price": None,
    "source_quality": None,
    "draft_note": "LLM gateway unavailable; no extraction performed.",
    "warnings": [
        "OmniRoute gateway unavailable. No extraction performed.",
        "This candidate will not have normalized fields until extraction is run.",
    ],
}

# ---------------------------------------------------------------------------
# Known land-type codes commonly found in Vietnamese listings
# ---------------------------------------------------------------------------
_VIETNAMESE_LAND_TYPES = [
    "ONT", "ODT", "CLN", "BHK", "TMDV", "SKC", "NK", "LNK",
    "PNN", "DGT", "DDT", "NTS", "RSX", "MNC", "LUC", "HNK",
]

_FALLBACK_RATIONALE = {
    "rationale": "LLM gateway unavailable; no rationale generated.",
    "strengths": [],
    "weaknesses": [],
    "adjustment_notes": [],
    "warnings": ["OmniRoute gateway unavailable. No rationale generated."],
}

