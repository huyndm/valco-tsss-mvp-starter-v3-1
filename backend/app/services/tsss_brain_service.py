def _subject_query_hint(subject_asset: SubjectAsset) -> dict[str, Any]:
    """Build deterministic search query hints from subject asset data."""
    size = subject_asset.size_sqm
    area = subject_asset.market_area
    land = subject_asset.land_type
    return {
        "search_queries": [
            f"Nhà đất bán tại {area}, loại đất {land}",
            f"Đất {land} thổ cư {area} diện tích {size} m²",
            f"Bất động sản {land} {area} giá tốt",
            f"So sánh giá đất {land} khu vực {area}",
            f"Giao dịch {land} thành công {area}",
        ],
    }


def _safe_float(value: Any) -> float | None:
    """Safely convert a value to float or return None."""
    if value is None:
        return None
    try:
        return float(value)
    except (ValueError, TypeError):
        return None


def _parse_vietnamese_number(text: str) -> float | None:
    """Parse a Vietnamese numeric expression (e.g. '11.4 tỷ', '95 triệu') to float.

    Handles both decimal separators (e.g. 11.4 → 11.4) and thousand
    separators (e.g. 1.000.000 → 1000000).
    """
    if not text:
        return None
    text = text.strip().lower().replace(",", "")
    # Ordered by descending multiplier so we match "tỷ" before plain digits
    multipliers = [
        (r'([\d.]+)\s*tỷ', 1_000_000_000),
        (r'([\d.]+)\s*triệu', 1_000_000),
        (r'([\d.]+)\s*trieu', 1_000_000),
        (r'([\d.]+)\s*nghìn', 1_000),
        (r'([\d.]+)\s*ngàn', 1_000),
    ]
    for pattern, multiplier in multipliers:
        m = re.search(pattern, text)
        if m:
            raw_num = m.group(1)
            try:
                return _parse_decimal_or_thousand(raw_num) * multiplier
            except ValueError:
                continue
    # Try bare number
    m = re.search(r'([\d.]+)', text)
    if m:
        try:
            return _parse_decimal_or_thousand(m.group(1))
        except ValueError:
            return None
    return None


def _parse_decimal_or_thousand(num_str: str) -> float:
    """Convert a string like '11.4' or '1.000.000' to float.

    - Single '.' followed by 1-2 trailing digits → decimal separator (keep).
    - Otherwise → thousand separator (remove all '.').
    """
    parts = num_str.split(".")
    if len(parts) == 2 and len(parts[1]) in (1, 2):
        # Likely a decimal separator, e.g. "11.4"
        return float(num_str)
    # Otherwise treat as thousand separators, e.g. "1.000.000"
    return float(num_str.replace(".", ""))


def _fallback_parse_raw_text(
    raw_text: str,
    subject_asset: SubjectAsset | None = None,
) -> dict[str, Any]:
    """Deterministic regex-based extraction from Vietnamese real-estate text.

    Used when the LLM gateway is unavailable.  Only fields that can be
    confidently extracted are set; everything else remains ``None``.
    """
    result: dict[str, Any] = {
        "market_area": None,
        "land_type": None,
        "planning_segment": None,
        "size_sqm": None,
        "unit_price": None,
        "asking_price": None,
        "source_quality": None,
    }

    text_lower = raw_text.lower()

    # ── size_sqm ──────────────────────────────────────────────────────
    # "diện tích 120m2", "120 m²", "120m2"
    size_patterns = [
        r'diện\s*tích\s*([\d.]+)\s*m',
        r'(\d+)\s*m[²2]',
    ]
    for pat in size_patterns:
        m = re.search(pat, text_lower)
        if m:
            try:
                result["size_sqm"] = float(m.group(1).replace(".", ""))
            except ValueError:
                pass
            if result["size_sqm"] is not None:
                break

    # ── land_type ─────────────────────────────────────────────────────
    # "đất ONT", "đất ODT", "loại đất ONT"
    # Use original (non-lowercased) text since land-type codes are uppercase
    for lt in _VIETNAMESE_LAND_TYPES:
        if re.search(rf'\b{lt}\b', raw_text, re.IGNORECASE):
            result["land_type"] = lt
            break

    # ── unit_price (VND/m²) ───────────────────────────────────────────
    # "đơn giá khoảng 95 triệu đồng/m2"
    # "đơn giá ... 95 triệu/m2"
    # "95 triệu đồng/m2"
    unit_price_patterns = [
        r'đơn\s*giá.*?([\d.]+\s*(?:triệu|tỷ|trieu)\s*(?:đồng)?\s*/?\s*m[²2])',
        r'([\d.]+\s*(?:triệu|tỷ|trieu)\s*(?:đồng)?\s*/?\s*m[²2])',
    ]
    for pat in unit_price_patterns:
        m = re.search(pat, text_lower)
        if m:
            val = _parse_vietnamese_number(m.group(1))
            if val is not None:
                result["unit_price"] = val
                break

    # ── asking_price (VND) ────────────────────────────────────────────
    # "giá bán 11.4 tỷ đồng"  OR  "giá 11.4 tỷ"
    # Avoid matching the unit_price pattern again.
    m_ask = re.search(r'giá\s*(?:bán)?\s*([\d.]+\s*(?:tỷ|triệu|trieu)\s*(?:đồng)?)', text_lower)
    if m_ask:
        val = _parse_vietnamese_number(m_ask.group(1))
        if val is not None:
            # Only accept as asking_price if it's significantly different from unit_price
            # (unit_price is VND/m², asking_price is total VND)
            if result["unit_price"] is None or abs(val - result["unit_price"]) > 1000:
                result["asking_price"] = val

    # ── market_area ───────────────────────────────────────────────────
    # Try to extract location from the beginning of the text
    # "Bán đất Phước Long, TP Thủ Đức" -> "Phước Long, TP Thủ Đức"
    m_area = re.search(
        r'(?:bán\s*(?:đất|nhà|cc|chung\s*cư)\s*)?([\w\s,]+?)'
        r'(?:\.|\s+diện\s*tích|\s+đất\s|\s+giá\s|\s+đơn\s*giá)',
        text_lower,
    )
    if m_area:
        candidate = m_area.group(1).strip().strip(",").strip()
        if candidate and len(candidate) > 3:
            result["market_area"] = candidate

    # ── source_quality ────────────────────────────────────────────────
    # Default MEDIUM if any fields were parsed; otherwise leave None
    if any(v is not None for v in result.values()):
        result["source_quality"] = "MEDIUM"

    return result
